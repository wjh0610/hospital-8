package com.aaa.model;


public class Docpost {

  private long postid;
  private String name;
  private String sal;


  public long getPostid() {
    return postid;
  }

  public void setPostid(long postid) {
    this.postid = postid;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getSal() {
    return sal;
  }

  public void setSal(String sal) {
    this.sal = sal;
  }

}
